package java_practice_package_1;

public class swap {
	
	
	public static void main(String[] args) {
			int a=10;
			int b=12;
			System.out.println(a);
			System.out.println(b);
			
			
			a=a+b;
			b=a-b;
			a=a-b;
			
			System.out.println(a);
			System.out.println(b);
			
	}
}
